package com.dev.obj;
public class Call {

	private String send;
	private String key;
	private String value;

	public String getSend() {
		return send;
	}
	public void setSend(String send) {
		this.send = send;
	}
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	
	public Call() {
		super();
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
}